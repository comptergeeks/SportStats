package com.company;

public class BasicPlayer {
    private String name;
    private double salary;
    private int number;

    public BasicPlayer(String name, double salary, int number) {
        this.name = name;
        this.salary = salary;
        this.number = number;
    }
    public double getSalary() {
        return this.salary = salary;
    }
    public String getName() {
        return this.name;
    }
    public int getNumber() {
        return this.number;
    }

}
